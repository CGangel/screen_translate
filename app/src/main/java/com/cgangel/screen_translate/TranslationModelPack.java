package com.cgangel.screen_translate;

public class TranslationModelPack {
    public final String id;
    public final String sourceTag;
    public final String targetTag;

    public TranslationModelPack(String id, String sourceTag, String targetTag) {
        this.id = id;
        this.sourceTag = sourceTag;
        this.targetTag = targetTag;
    }
}
